export default function readPrescriptionPersistencePayload({
  prescriptionValues,
  prescriptionPreview,
  createdByEmail,
  createdByUid,
  physicianEmail,
  status,
  transactionId,
  pdfUrl,
  pdfFileName,
}) {
  return {
    transactionId,
    status,
    createdByEmail,
    createdByUid,
    physicianEmail: physicianEmail || createdByEmail,
    patientName: prescriptionValues.patientName,
    patientAge: prescriptionValues.patientAge,
    patientSex: prescriptionValues.patientSex,
    drugName: prescriptionValues.drugName,
    drugDosage: prescriptionValues.drugDosage,
    dosePerIntake: prescriptionValues.dosage,
    frequency: prescriptionValues.frequency,
    route: prescriptionValues.route,
    duration: prescriptionPreview.durationDisplayValue,
    durationValue: prescriptionValues.durationValue,
    durationUnit: prescriptionValues.durationUnit,
    quantity: prescriptionValues.quantity,
    signa: prescriptionValues.instructions,
    clinicalNotes: prescriptionValues.notes,
    pdf: pdfUrl || "",
    pdfFileName: pdfFileName || "",
    createdAtIso: new Date().toISOString(),
    syncStatus: status === "completed" ? "pending" : "draft",
  };
}
